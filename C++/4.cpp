


#include <iostream>
using namespace std;
using namespace std;

int main()
{
	int x = 8, y = 2; // x değişkeni 8, y değişkeni 2 olarak tanımlanır
	x *= y + 1; // x = x * (y + 1) işlemi yapılır, yani x = 8 * (2 + 1) = 24
	cout << x / y << endl; // x / y işlemi yapılır, yani 24 / 2 = 12 ve ekrana yazdırılır
	cout << ++x << endl;// x değişkeni 1 artırılır (x = 25) ve ekrana yazdırılır
	cout << x << endl;// x değişkeninin güncel değeri (25) ekrana yazdırılır

}
